export function computeFleetStats(
  vehicleList: Vehicle[],
  alertList: Alert[]
): FleetStats {
  const active = vehicleList.filter((v) => v.status === 'active').length;
  const idle = vehicleList.filter((v) => v.status === 'idle').length;
  const maintenance = vehicleList.filter(
    (v) => v.status === 'maintenance'
  ).length;
  const offline = vehicleList.filter((v) => v.status === 'offline').length;
  const totalDistance = vehicleList.reduce((s, v) => s + v.distanceToday, 0);
  const avgFuel =
    vehicleList.reduce((s, v) => s + v.fuelLevel, 0) / vehicleList.length;
  const unacked = alertList.filter((a) => !a.acknowledged);
  return {
    totalVehicles: vehicleList.length,
    activeVehicles: active,
    idleVehicles: idle,
    maintenanceVehicles: maintenance,
    offlineVehicles: offline,
    totalDistanceToday: totalDistance,
    averageFuelLevel: Math.round(avgFuel),
    activeAlerts: unacked.length,
    criticalAlerts: unacked.filter((a) => a.severity === 'critical').length,
  };
}
